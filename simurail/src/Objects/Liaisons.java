package Objects;

public class Liaisons {

    Object object1, object2;
    int x1, y1, z1, x2, y2, z2;
    String type;

    Liaisons(Object object1, int x, int y, int z, Object object2, int x2, int y2, int z2, String type)
    {
        this.object1 = object1;
        this.object2 = object2;
        this.x1 = x;
        this.y1 = y;
        this.z1 = z;
        this.x2 = x2;
        this.y2 = y2;
        this.z2 = z2;
        this.type = type;
    }







}
